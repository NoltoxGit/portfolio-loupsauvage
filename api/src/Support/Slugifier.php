    public static function slugify(string $value): string
    {
        $normalized = strtr(trim($value), self::TRANSLITERATION_MAP);
        $ascii = function_exists('iconv') ? iconv('UTF-8', 'ASCII//TRANSLIT//IGNORE', $normalized) : false;
        $normalized = strtolower(is_string($ascii) ? $ascii : $normalized);
        $normalized = preg_replace('/[\'’`]+/', '', $normalized) ?? '';
        $normalized = preg_replace('/[^a-z0-9]+/', '-', $normalized) ?? '';
        $normalized = trim($normalized, '-');

        return preg_replace('/-+/', '-', $normalized) ?? '';
    }
